# 7. Explain internal working of python
#  Ans= Internal Working of Python
# Source Code (.py)
# You write your Python program in a .py file.
# Compilation to Bytecode
# The Python interpreter first compiles the code into bytecode, a low-level set of instructions stored in .pyc files


# Python Virtual Machine (PVM)


# The PVM (Python Virtual Machine) executes this bytecode line by line.


# It converts the bytecode into machine code that the computer can understand.


# Execution and Output


# Finally, the machine executes the instructions, and you see the program output on your screen
# Python code → Compiled to Bytecode → Executed by PVM → Output displayed

